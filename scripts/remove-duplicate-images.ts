/**
 * Script to remove duplicate images from the Louis Vuitton products array
 * Duplicates are identified by images with (1), (2), etc. suffixes where a base version exists
 */

type Product = {
  id: number
  name: string
  price: string
  image: string
}

/**
 * Extract the base image name without the (1), (2) suffix
 * Examples:
 * - "089c3f9b-6692-47be-b908-1ad32533185e(1).jpg" -> "089c3f9b-6692-47be-b908-1ad32533185e.jpg"
 * - "089c3f9b-6692-47be-b908-1ad32533185e.jpg" -> "089c3f9b-6692-47be-b908-1ad32533185e.jpg"
 */
function getBaseImageName(image: string): string {
  return image.replace(/\(\d+\)\./, '.')
}

/**
 * Check if an image is a duplicate (has a suffix like (1), (2))
 */
function isDuplicate(image: string): boolean {
  return /\(\d+\)\./.test(image)
}

/**
 * Remove duplicate images from the products array
 * Keeps products with base image names, removes those with (1), (2) suffixes
 */
function removeDuplicates(products: Product[]): Product[] {
  // Create a map of base image names to track what exists
  const baseImagesMap = new Map<string, boolean>()
  
  // First pass: identify all base images
  products.forEach(product => {
    const baseImage = getBaseImageName(product.image)
    if (!isDuplicate(product.image)) {
      baseImagesMap.set(baseImage, true)
    }
  })
  
  // Second pass: filter out duplicates if base version exists
  const filtered = products.filter(product => {
    if (isDuplicate(product.image)) {
      const baseImage = getBaseImageName(product.image)
      // Remove if base version exists
      if (baseImagesMap.has(baseImage)) {
        console.log(`Removing duplicate: ${product.image} (base exists: ${baseImage})`)
        return false
      }
    }
    return true
  })
  
  // Reassign sequential IDs
  return filtered.map((product, index) => ({
    ...product,
    id: index + 1
  }))
}

